export function openJsonExampleModal(): void {
  const modal = document.getElementById("jsonExampleModal");
  if (modal) {
    modal.style.display = "flex";
    bindJsonExampleEvents();
  }
}

export function closeJsonExampleModal(): void {
  const modal = document.getElementById("jsonExampleModal");
  if (modal) {
    modal.style.display = "none";
  }
}

function bindJsonExampleEvents(): void {
  const modal = document.getElementById("jsonExampleModal");
  if (!modal) return;

  // Close button
  const closeBtn = modal.querySelector(".json-example-modal-close");
  if (closeBtn) {
    closeBtn.addEventListener("click", () => closeJsonExampleModal());
  }

  // Click outside modal to close
  modal.addEventListener("click", (e) => {
    if (e.target === modal) {
      closeJsonExampleModal();
    }
  });

  // Helper function to handle example selection
  const selectExample = (card: Element | null) => {
    if (card) {
      const exampleType = card.getAttribute("data-example");
      if (exampleType) {
        window.dispatchEvent(new CustomEvent("useJsonExample", { detail: { type: exampleType } }));
        closeJsonExampleModal();
      }
    }
  };

  // Use example buttons
  const useButtons = modal.querySelectorAll(".json-example-use-btn");
  useButtons.forEach((btn) => {
    btn.addEventListener("click", (e) => {
      const card = (e.target as HTMLElement).closest(".json-example-card");
      selectExample(card);
    });
  });

  // Click on preview to use example
  const previews = modal.querySelectorAll(".json-example-preview");
  previews.forEach((preview) => {
    preview.addEventListener("click", (e) => {
      const card = (e.target as HTMLElement).closest(".json-example-card");
      selectExample(card);
    });
  });
}

export function getJsonExampleContent(exampleType: string): string | null {
  const examples = getJsonExamples();
  const example = examples[exampleType];

  if (example) {
    return JSON.stringify(example.data, null, 2);
  }

  return null;
}
